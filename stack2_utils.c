#include "push_swap.h"

void  ss(l_list *a, l_list *b)
{
	sa(a);
	sb(b);
}

void rr(l_list *a, l_list *b)
{
	ra(&a);
	rb(&b);
}

void rrr(l_list *a, l_list *b)
{
	rra(&a);
	rrb(&b);
}
